#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		x;

	x = 0;
	while (src[x] != '\0')
		x++;
	dest = (char*)malloc(sizeof(char) * (x + 1));
	x = 0;
	while (src[x] != '\0')
	{
		dest[x] = src[x];
		x++;
	}
	dest[x] = '\0';
	return (dest);
}
